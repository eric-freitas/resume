/**
 * info to render a font-awesome icon
 *
 * @export
 * @interface IconInfo
 */
export interface IconInfo {
    /**
     * library of the icon
     *
     * @type {string}
     * @memberof IconInfo
     */
    lib     : string,
    /**
     * name of the icon
     *
     * @type {string}
     * @memberof IconInfo
     */
    name    : string
}